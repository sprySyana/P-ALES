        <h2>A propos</h2>
        <h3>le projet P-ALES</h3>
        <h4>Descriptif</h4>
        <p>Dépôt Github du projet : <a href="https://github.com/sprySyana/P-ALES">Github P-ALES</a></p>
        <p>Ce projet a été réalisé dans le cadre du Master 2 
            <a href="http://formations.univ-grenoble-alpes.fr/fr/catalogue/master-XB/
            sciences-humaines-et-sociales-SHS/master-sciences-du-langage-program-master-sciences-du-langage/
            parcours-industries-de-la-langue-subprogram-parcours-industries-de-la-langue.html">Industries de la Langue</a>
             de l'Université Grenoble-Alpes en tant que projet professionnel.</p>
        <p>Il fait suite au projet <a href="https://github.com/DaronnatMelnikova/Phonochrome">Phonochrome</a>.</p>
        <p>L'objectif est de calculer automatiquement un set de couleurs optimal pour représenter les phonèmes.</p>
        <p>Le choix d'une représentation chromatique des phonèmes s'inscrit dans la ligne de réflexion de <a href="http://kinephones.u-grenoble3.fr">Kinéphones</a>.</p>
        <h4>Participants</h4>
